export function countryList() {
  let zh = null;
  let en = null;
  try { zh = new Intl.DisplayNames(['zh-Hant'], { type: 'region' }); } catch { /* 舊瀏覽器 */ }
  try { en = new Intl.DisplayNames(['en'], { type: 'region' }); } catch { /* 舊瀏覽器 */ }
  const out = [];
  for (const code of REGION_CODES) {
    let zhName = code;
    let enName = code;
    try { zhName = (zh && zh.of(code)) || code; } catch { /* 略過無效代碼 */ }
    try { enName = (en && en.of(code)) || code; } catch { /* 略過無效代碼 */ }
    if (zhName === code && enName === code) continue;
    out.push({ code, zh: zhName, en: enName });
  }
  out.sort((a, b) => a.zh.localeCompare(b.zh, 'zh-Hant'));
  return out;
}
