// 引號內文字去尾句號，避免「……。」。的重複標點
function stripEnd(s) {
  return String(s || '').replace(/[。．\s]+$/, '');
}

// 理解回照的開場語（離線版：引用玩家的話拼出回照）
export function offlineMirror(state) {
  const answered = state.turns.filter((t) => t.answer);
  const longest = answered.slice().sort((a, b) => b.answer.length - a.answer.length)[0];
  const parts = ['我想先確認我有沒有聽對。'];
  parts.push(`你帶來的是——「${stripEnd(state.opening.slice(0, 60))}${state.opening.length > 60 ? '…' : ''}」。`);
  if (longest) {
    parts.push(`一路聽下來，最有份量的是你說的：「${stripEnd(longest.answer.slice(0, 50))}${longest.answer.length > 50 ? '…' : ''}」`);
  }
  if (answered.length < state.turns.length) {
    parts.push('有些問題你選擇先放著不答——那也是一種訊息，我會把那份安靜一起放進理解裡。');
  }
  parts.push('如果有哪裡不對、或還有想補充的，請直接告訴我；沒有的話，我們就往下走。');
  return parts.join('\n\n');
}

